import math

resul1 = math.floor(89.66) # redondear hacia abajo
print(resul1) # 89

resul2 = math.ceil(89.66) # redondear hacia arriba
print(resul2) # 90

pi = math.pi # averiguar pi
print(pi) # 3.141592653589793

resul3 = math.log(100, 5) # averiguar logaritmo
print(resul3) # 2.8613531161467867

resul4 = math.tan(2565) # averiguar tangente de un numero
print(resul4) # 9.02100465179493

resul5 = math.cos(2565) # averiguar cosciente de un numero
print(resul5) # 0.11017752046756596

