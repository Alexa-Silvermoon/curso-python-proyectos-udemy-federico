
# operaciones normales:
x = float( input("Ingrese un numero: ") );
y = float( input("Ingrese otro numero: ") );
print( f"la suma de { x } + { y } es: { x + y }" );
print( f"la resta de { x } - { y } es: { x - y }" );
print( f"la multiplicacion de { x } * { y } es: { x * y }" );
print( f"la division de { x } / { y } es: { x / y }" );

# division al piso:
q = 6;
w = 2;
e = 3;
print(f"{ q } dividido normalmente entre { w } es: { q / w }" );
print( f"{ q } dividido al piso entre { w } es: { q // w }" ); # dividiar al piso es dividir hacia abajo

# saber si es par o impar, es decir sacar el modulo
print( f"{ q } modulo de { w } es: { q % w }, si es 1 es impar, si es 0 es par");

# multiplicar al cuadrado:
print(f"{ q } elevado a la { w } es: { q ** w }" ); # multiplicar al cuadrado

# multiplicar al cubo:
print(f"{ q } elevadoa a la { 3 }(cubo) es: { q ** e }" ); # multiplicar al cubo

# sacar raiz cuadrada:
print( f"la raiz cuadrada de { q } es: { q ** 0.5 }" ); # es necesario ese 00.5 para raiz

